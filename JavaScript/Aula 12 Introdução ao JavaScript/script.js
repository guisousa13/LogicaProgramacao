// Variaveis - VAR e LET
// Declarando o VAR
var texto = "Hello World" 
console.log(texto)

//Declarando Let
let nome = "Guilherme"
console.log("Nome:",nome)

//Atribuindo um novo valor = LET
nome = "Guilherme"
console.log("Nome:",nome)

//Atribuindo um novo valor = VAR
texto = "Olá, Mundo" 
console.log(texto)

// Constantes - const
const valorPi = 3.14
console.log("O valor de PI é igual a",valorPi)
